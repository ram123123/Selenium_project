
public class Swithcase111 {
	
	public static void main(String[] args) 
	{
		int ch=2;
		
		switch (ch) {
		case 1:System.out.println("first");
		break;
		
		case 2:System.out.println("second");
		break;
		
		case 3:System.out.println("third");
		break;
			

		default:System.out.println("wrong");
			break;
			
				
		}
	}

}
